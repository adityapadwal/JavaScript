// 1. Object Literal
// The object literal syntax is the simplest and most commonly used way to create objects. 
// It uses curly braces to define an object.

const object = {
    "name": "Aditya",
    "age": 22,
    Salary: 12345,
    greet: function() {
        console.log("Hello from object");
    }
}

console.log(object.name);
console.log(object.Salary);

object.greet();